    <h2 class="subTitle">Summary</h2>
    <div class="textBlock">Invented in 2005, the hydraulophone is a tonal acoustic musical instrument played by direct physical contact with water (sometimes other fluids) where sound is generated or affected hydraulically. The hydraulophone was described and named by Steve Mann in 2005, and patented in 2011. Typically, sound is produced by the same hydraulic fluid in contact with the player's fingers. It has been used as a sensory exploration device for low vision individuals.</div>
    <h2 class="subTitle">History</h2>
    <div class="textBlock">In an interview with WaterShapes, Steve Mann says that the idea for creating a hydraulophone developed gradually in the 1980s. He remembers a specific event that was important in the genesis of the new instrument. He was watching a truck filling some tanks with liquid nitrogen and was intrigued by the sounds produced as the fluid flowed through the pipes.</div>
    <h2 class="subTitle">Operation</h2>
    <div class="textBlock">Hydraulophones use water-flow sound-producing mechanisms. They have a user interface, which is blocking water jets to produce sound. Blocking flow through a finger hole directs the water instead to one or more of the sound-production mechanisms, or resulting changes in flow or pressure affect a separate sounding mechanism associated with each finger hole. Blocking water from coming out of a given hole produces a given note, which, in some embodiments, is electrically amplified by a hydrophone.</div>
