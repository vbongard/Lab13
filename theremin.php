    <h2 class="subTitle">Summary</h2>
    <div class="textBlock">Invented in 1920, the theremin is an electronic musical instrument controlled without physical contact by the thereminist (performer). It is named after the Westernized name of its Soviet inventor, Léon Theremin, who patented the device in 1928. The instrument's controlling section usually consists of two metal antennas that sense the relative position of the thereminist's hands and control oscillators for frequency with one hand, and amplitude (volume) with the other. The electric signals from the theremin are amplified and sent to a loudspeaker. </div>
    <h2 class="subTitle">History</h2>
    <div class="textBlock">The theremin was originally the product of Soviet government-sponsored research into proximity sensors. The instrument was invented by a young Russian physicist named Lev Sergeyevich Termen (known in the West as Léon Theremin) in October 1920 after the outbreak of the Russian Civil War.</div>
    <h2 class="subTitle">Operation</h2>
    <div class="textBlock">Important in theremin articulation is the use of the volume control antenna. Unlike touched instruments, where simply halting play or damping a resonator in the traditional sense silences the instrument, the thereminist must "play the rests, as well as the notes", as Clara Rockmore observed. If the pitch hand is moved between notes, without first lowering the volume hand, the result is a "swooping" sound akin to a swanee whistle or a glissando played on the violin. Small flutters of the pitch hand can be used to produce a vibrato effect. To produce distinct notes requires a pecking action with the volume hand to mute the volume while the pitch hand moves between positions. </div>
